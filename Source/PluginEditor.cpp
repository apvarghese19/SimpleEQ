SimpleEQAudioProcessorEditor::SimpleEQAudioProcessorEditor (SimpleEQAudioProcessor& p)
    : AudioProcessorEditor (&p), audioProcessor (p),
    peakFreqSliderAttachment(audioProcessor.apvts, "peak_freq", peakFreqSlider),
    peakGainSliderAttachment(audioProcessor.apvts, "peak_gain", peakGainSlider),
    peakQualitySliderAttachment(audioProcessor.apvts, "peak_quality", peakQualitySlider),
    lowCutFreqSliderAttachment(audioProcessor.apvts, "low_cut_freq", lowCutFreqSlider),
    highCutFreqSliderAttachment(audioProcessor.apvts, "high_cut_freq", highCutFreqSlider),
    lowCutSlopeSliderAttachment(audioProcessor.apvts, "low_cut_slope", lowCutSlopeSlider),
    highCutSlopeSliderAttachment(audioProcessor.apvts, "high_cut_slope", highCutSlopeSlider)
{
    // Make sure that before the constructor has finished, you've set the
    // editor's size to whatever you need it to be.
    
    for (auto *comp : getComps()){
        addAndMakeVisible(comp);
    }
    setSize(600, 400);
}

SimpleEQAudioProcessorEditor::~SimpleEQAudioProcessorEditor()
{
}

